    public void ejecutar() {
        if (varComando.existeComando(txtEntrada.getText())) {
            server.ejecutar(txtEntrada.getText().trim().toLowerCase());
        } else {
            txtVerMensajes.append("root@Servidor:~$ "+ txtEntrada.getText() + "\n   No se reconoce el comando: " + txtEntrada.getText() + "\n");
        }
        txtEntrada.setText("");
    }

